# Programa para calcular a área de um triângulo

# Solicitar ao usuário a base e a altura do triângulo
base = float(input(" valor da base do triângulo: "))
altura = float(input("valor da altura do triângulo: "))

# Calcula a área usando a fórmula: área = (base * altura) / 2
area = (base * altura) / 2

# mostrando resultado
print("A área do triângulo é: {area}")
